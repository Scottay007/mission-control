'use client';
import { useState } from 'react';

const F = [
  {
    name: "Won't Start",
    steps: [
      {
        question: "Starter engage?",
        options: [
          { label: "Yes - spins", next: 1 },
          { label: "No - click/silence", next: 2 }
        ]
      },
      {
        question: "Fuel pump hum?",
        conclusion: "No fuel: Check pump fuse, relay, pump. Check for DTCs."
      },
      {
        question: "Battery voltage?",
        conclusion: "Below 12V: charge/replace. Check parasitic drain."
      }
    ]
  },
  {
    name: "Check Engine",
    steps: [
      {
        question: "Steady or flashing?",
        options: [
          { label: "Steady", next: 1 },
          { label: "Flashing", next: 1 }
        ]
      },
      {
        question: "Read codes",
        conclusion: "Use scan tool. Common: P0131, P0122, P0562. Flashing = misfire."
      }
    ]
  },
  {
    name: "Rough Idle",
    steps: [
      {
        question: "Vacuum leak?",
        conclusion: "Spray carb cleaner around intake. Also clean IAC and throttle body."
      }
    ]
  }
];

export default function HDFlow() {
  const [af, setAf] = useState(0);
  const [st, setSt] = useState(0);
  const fl = F[af];
  const s = fl?.steps?.[st];

  const activeClass = 'px-3 py-1.5 rounded-full text-xs font-medium text-white';
  const inactiveClass = 'px-3 py-1.5 rounded-full text-xs font-medium bg-surface text-text-muted border border-border';

  return (
    <div className="min-h-screen bg-background">
      <header className="border-b border-border px-4 py-3">
        <div className="max-w-4xl mx-auto flex items-center gap-3">
          <a href="/harley" className="text-text-muted hover:text-text-primary text-sm">
            ‚Üê Harley
          </a>
          <span className="text-border">|</span>
          <h1 className="font-bold" style={{ color: '#E8720E' }}>
            üîß Diagnostic Flowcharts
          </h1>
        </div>
      </header>
      <div className="max-w-4xl mx-auto px-4 py-8">
        <h2 className="text-2xl font-bold mb-2">Diagnostic Flowcharts</h2>
        <p className="text-text-muted text-sm mb-6">Step-by-step Harley diagnosis.</p>
        <div className="flex flex-wrap gap-2 mb-8">
          {F.map((f, i) => (
            <button
              key={i}
              onClick={() => { setAf(i); setSt(0); }}
              className={af === i ? activeClass : inactiveClass}
              style={af === i ? { backgroundColor: '#E8720E' } : {}}
            >
              {f.name}
            </button>
          ))}
        </div>
        {s && (
          <div className="mc-card p-6">
            <div className="text-text-dim text-xs mb-2">Step {st + 1}</div>
            <h3 className="text-xl font-bold mb-4">{s.question}</h3>
            <div className="space-y-2">
              {s.options?.map((o, j) => (
                <button
                  key={j}
                  onClick={() => setSt(o.next)}
                  className="mc-card-hover p-3 w-full text-left text-sm"
                >
                  {o.label}
                </button>
              ))}
            </div>
            {s.conclusion && (
              <div className="mt-4 p-4 rounded-mc border-2" style={{ borderColor: '#E8720E40' }}>
                <p className="font-bold mb-1" style={{ color: '#E8720E' }}>Diagnosis:</p>
                <p className="text-text-secondary text-sm whitespace-pre-line">{s.conclusion}</p>
              </div>
            )}
            {st > 0 && (
              <button onClick={() => setSt(0)} className="mt-4 text-sm text-text-muted">
                ‚Ü© Start Over
              </button>
            )}
          </div>
        )}
      </div>
    </div>
  );
}